'''---- Operadores ----'''

''' ARITIMÉTICOS '''
x = 15 
y = 5
print('\n')
print('Resultado dos operadores aritiméticos')
print(x + y) #adição
print(x - y) #subtração
print(x * y) #multiplicação
print(x / y) #divisão
print(x ** y) #potenciação
print(x // y) #divisão inteira
print(x % y ) #resto da divisão
print('\n')

''' LÓGICOS '''
tem_cafe = True
tem_pao = False
print('Resultado dos operadores lógicos')
print(not tem_cafe) #operador de negação (not)
print(tem_cafe or tem_pao) #operador OU (or)
print(tem_cafe and tem_pao) #operador de exclusividade (and)
print('\n')

''' RELACIONAIS '''
dolar = 5.3
real = 1
print('Resultado dos operadores relacionais')
print(dolar > real) #maior
print(dolar < real) #menor
print(dolar == real) #igualdeda
print(dolar >= real) #maior ou igual
print(dolar <= real) #mennor ou igual
print(dolar != real) #diferente