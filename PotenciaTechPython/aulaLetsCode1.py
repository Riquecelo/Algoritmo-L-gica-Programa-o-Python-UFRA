'''Tipos de Variáveis
# Operador de atribuição = (igual)'''
#exemplo:

x = 15
print(x)

'''  Inteiros
# função type() revelo o tipo de dado da variável'''
# exemplo:
x = -5
print(type(x))
print(x)
print(type(x))

''' Float: valores decimais'''
# exemplo
preco = 19.99
print(preco)
print(type(preco))

''' Strings: cadeia de caracteres, utilizam aspas simples ou duplas
'''
# exemplo:
nome = 'Marcelo'
print(nome)
print(type(nome))

''' Booleano: possui apenas dois valores, true ou false'''
# exemplo:
disponivel= True
print(disponivel)
print(type(disponivel))